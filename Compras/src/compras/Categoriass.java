    public void ListarCategorias() {
        List<Categorias> ListarCa = cat.ListarCategorias();
        modelo = (DefaultTableModel) TableCategorias.getModel();
        Object[] ob = new Object[3];
        for (int i = 0; i <ListarCa.size(); i++) 
        {
          
            ob[0] = ListarCa.get(i).getIdCategoria();
            ob[1] = ListarCa.get(i).getNombre();
            ob[2] = ListarCa.get(i).getEstatus();
            modelo.addRow(ob);
            
        }
        TableCategorias.setModel(modelo);
    }

