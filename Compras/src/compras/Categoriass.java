    private void TableCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableCategoriasMouseClicked
        int fila = TableCategorias.rowAtPoint(evt.getPoint());
        txtId.setText(TableCategorias.getValueAt(fila, 0).toString());
        txtnombre.setText(TableCategorias.getValueAt(fila, 1).toString());
        txtestatus.setText(TableCategorias.getValueAt(fila, 2).toString());
    }//GEN-LAST:event_TableCategoriasMouseClicked

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if (!"".equals(txtId.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "decea eliminarlo");
            if (pregunta == 0) {
                if (!"".equals(txtnombre.getText()) || !"".equals(txtestatus.getText()))
                    ca.setIdCategoria(Integer.parseInt(txtId.getText()));
                ca.setEstatus(txtestatus.getText());
                cat.EliminarCategorias(ca);
                 JOptionPane.showMessageDialog(null, "eliminacion exitosa");
                LimpiarTable();
                LimpiarCategorias();
                ListarCategorias();
            }else
                JOptionPane.showMessageDialog(null, "eliminacion interrumpida");
        }
        
 
    }//GEN-LAST:event_btneliminarActionPerformed
    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    private void LimpiarCategorias() {
        txtId.setText("");
        txtnombre.setText("");
        txtestatus.setText("");
    }
    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (!"".equals(txtnombre.getText())
                && !"".equals(txtestatus.getText())) {

            ca.setNombre(txtnombre.getText());
            ca.setEstatus(txtestatus.getText());
            cat.RegistrarCategorias(ca);
            LimpiarTable();
            LimpiarCategorias();
            ListarCategorias();
            JOptionPane.showMessageDialog(null, "producto registrado");
        } else {
            JOptionPane.showMessageDialog(null, "los campos estan vacios");
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        if ("".equals(txtId.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }
        int pregunta = JOptionPane.showConfirmDialog(null, "Estas seguro de actualizarlo");
        if (pregunta == 0) {
            if (!"".equals(txtnombre.getText())) {
                ca.setIdCategoria(Integer.parseInt(txtId.getText()));
                ca.setNombre(txtnombre.getText());
                ca.setEstatus(txtestatus.getText());

                cat.ModificarCategorias(ca);
                LimpiarTable();
                LimpiarCategorias();
                ListarCategorias();

            }
            {
            }
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

