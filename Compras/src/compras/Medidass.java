    private void txtbuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyPressed
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txtbuscar.getText())) {
                String nombre = txtbuscar.getText();
                ma = mat.BuscarMedidas(nombre);
                if (ma.getNombre() != null) {
                    txtid.setText("" + ma.getId());
                    txtnombre.setText("" + ma.getNombre());
                    txtsalida.setText(""+ma.getSalida());
                    txtestatus.setText("" + ma.getEstaus());
                } else {
                    txtid.setText("");
                    txtnombre.setText("");
                    txtsalida.setText("");
                    txtestatus.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "ingrese los datos del producto");
            }
        }
    }//GEN-LAST:event_txtbuscarKeyPressed
public void LimpiarTable()
{
    for (int i = 0; i<modelo.getRowCount(); i++) 
    {
        modelo.removeRow(i);
        i=i-1;
    }
}
public void LimpiarMedidas()
{
    txtid.setText("");
    txtnombre.setText("");
    txtsalida.setText("");
    txtestatus.setText("");
}
