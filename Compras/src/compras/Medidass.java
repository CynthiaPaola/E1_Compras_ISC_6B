    public void ListarMedidas()
    {
        List<Medidas> ListarMa=mat.ListarMedidas();
        modelo=(DefaultTableModel) TableMedidas.getModel();
        Object[]ob=new Object[4];
        for (int i = 0; i <ListarMa.size(); i++)
        {
            ob[0]=ListarMa.get(i).getId();
            ob[1]=ListarMa.get(i).getNombre();
            ob[2]=ListarMa.get(i).getSalida();
            ob[3]=ListarMa.get(i).getEstaus();
            modelo.addRow(ob);
        }
        TableMedidas.setModel(modelo);
    }
