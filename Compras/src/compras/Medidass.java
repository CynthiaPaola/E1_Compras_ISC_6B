    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
       if(!"".equals(txtid.getText()))
       {
           int pregunta=JOptionPane.showConfirmDialog(null,"Estas seguro de eliminarlo");
           if(pregunta==0)
           {
               int id=Integer.parseInt(txtid.getText());
               mat.EliminarMedidas(id);
               LimpiarTable();
               LimpiarMedidas();
               ListarMedidas();
                       
           }
       }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        if("".equals(txtid.getText()))
        {
            JOptionPane.showMessageDialog(null,"seleccione una fila");
        }
        int pregunta=JOptionPane.showConfirmDialog(null,"Estas seguro de modificar sus datos");
        if(pregunta==0)
        {
            if(!"".equals(txtnombre.getText()) || !"".equals(txtsalida.getText())
                    || !"".equals(txtestatus.getText()))
            {
                ma.setId(Integer.parseInt(txtid.getText()));
                ma.setNombre(txtnombre.getText());
                ma.setSalida(txtsalida.getText());
                ma.setEstaus(txtestatus.getText());
                
                mat.ModificarMedidas(ma);
                LimpiarTable();
                LimpiarMedidas();
                ListarMedidas();
            }
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

